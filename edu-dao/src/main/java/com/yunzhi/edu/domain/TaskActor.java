package com.yunzhi.edu.domain;

public class TaskActor {
	
	private String id;
	private boolean isDone;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public boolean isIsDone() {
		return isDone;
	}
	public void setIsDone(boolean isDone) {
		this.isDone = isDone;
	}

}
